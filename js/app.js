function pageAsResult(page) {
  const p = MANUAL_DATA.PAGES[String(page)];
  return p ? { page, label: p.label, text: p.text, toc: p.toc, deg: p.deg } : null;
}

function renderCurated(topic) {
  const primary = pageAsResult(topic.page);
  if (!primary) return;
  $("#status").textContent = `Verified quick reference for \u201C${topic.q}\u201D \u2014 opens the manual at the procedure.`;
  const terms = tokenizeQuery(topic.q);
  $("#best").innerHTML = resultCard(primary, terms, true);
  const rel = topic.rel.map(pageAsResult).filter(Boolean);
  $("#more").innerHTML = rel.length
    ? `<details open><summary>Related pages (${rel.length})</summary>${rel
        .map((r) => resultCard(r, terms))
        .join("")}</details>`
    : "";
  document.querySelectorAll("[data-page]").forEach((b) =>
    b.addEventListener("click", () => openPage(parseInt(b.dataset.page, 10), terms))
  );
  openPage(topic.page, terms);
}

/* ---------------- wiring ---------------- */

document.addEventListener("DOMContentLoaded", () => {
  const q = $("#q");
  const run = () => {
    const query = q.value.trim();
    if (query) renderResults(query);
  };

  $("#searchBtn").addEventListener("click", run);
  q.addEventListener("keydown", (e) => {
    if (e.key === "Enter") { e.preventDefault(); run(); }
  });
  $("#clearBtn").addEventListener("click", () => {
    q.value = "";
    $("#status").textContent = "Ask a question to begin.";
    $("#best").innerHTML = "";
    $("#more").innerHTML = "";
    q.focus();
  });

  // Curated quick-reference chips (verified destinations, not live search)
  $("#topics").innerHTML = CURATED.map(
    (t, i) => `<button class="topic" data-i="${i}">${escapeHtml(t.q)}</button>`
  ).join("");
  document.querySelectorAll(".topic").forEach((b) =>
    b.addEventListener("click", () => renderCurated(CURATED[parseInt(b.dataset.i, 10)]))
  );

  // Viewer controls
  $("#viewer-close").addEventListener("click", () => $("#viewer").close());
  $("#viewer-prev").addEventListener("click", () => openPage(viewerPage - 1, viewerTerms));
  $("#viewer-next").addEventListener("click", () => openPage(viewerPage + 1, viewerTerms));
  $("#viewer").addEventListener("click", (e) => {
    if (e.target === $("#viewer")) $("#viewer").close();
  });

  // Voice input — graceful: hidden if unsupported.
  const SR = window.SpeechRecognition || window.webkitSpeechRecognition;
  const voiceBtn = $("#voiceBtn");
  if (!SR) {
    voiceBtn.hidden = true;
  } else {
    let rec = null;
    voiceBtn.addEventListener("click", () => {
      if (rec) { rec.stop(); return; }
      rec = new SR();
      rec.lang = "en-US";
      rec.interimResults = false;
      voiceBtn.classList.add("listening");
      voiceBtn.textContent = "Listening…";
      rec.onresult = (e) => {
        q.value = e.results[0][0].transcript;
        run();
      };
      rec.onerror = () => {
        $("#status").textContent = "Voice input isn\u2019t available right now (it may need this device\u2019s offline speech pack). Type your question instead.";
      };
      rec.onend = () => {
        voiceBtn.classList.remove("listening");
        voiceBtn.textContent = "\uD83C\uDFA4 Speak";
        rec = null;
      };
      rec.start();
    });
  }

  $("#pagecount").textContent = `${MANUAL_DATA.N} searchable sections · ${Object.keys(MANUAL_DATA.PAGES).length} manual pages`;
});
