/* ---------------- text processing (must mirror build_index.py) ---------------- */

function stem(w) {
  if (w.length > 4 && w.endsWith("ies")) return w.slice(0, -3) + "y";
  if (w.length > 4 && /(ses|xes|zes|ches|shes)$/.test(w)) return w.slice(0, -2);
  if (w.length > 3 && w.endsWith("s") && !w.endsWith("ss")) w = w.slice(0, -1);
  if (w.length > 5 && w.endsWith("ing")) w = w.slice(0, -3);
  else if (w.length > 4 && w.endsWith("ed")) w = w.slice(0, -2);
  return w;
}

const STOP = new Set([
  "a","an","the","to","do","i","if","of","in","on","for","and","or","is","are",
  "what","how","when","where","who","why","with","from","that","this","it","as",
  "be","by","at","my","me","we","you","your","our","doe","should","can","could",
  "would","will","have","ha","had","about","into","out","up","get","got","need",
  "show","someone","something",
]);

